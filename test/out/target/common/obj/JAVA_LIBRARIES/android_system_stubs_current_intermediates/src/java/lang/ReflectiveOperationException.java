package java.lang;
public class ReflectiveOperationException
  extends java.lang.Exception
{
public  ReflectiveOperationException() { throw new RuntimeException("Stub!"); }
public  ReflectiveOperationException(java.lang.String message) { throw new RuntimeException("Stub!"); }
public  ReflectiveOperationException(java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }
public  ReflectiveOperationException(java.lang.String message, java.lang.Throwable cause) { throw new RuntimeException("Stub!"); }
}
